num1 = int(input("Enter first number "))
num2 = int(input("Enter second number "))

# ADDITION
sum = num1 + num2
print(f"sum is {sum}")

# SUBTRACTION
sub = num1 - num2
print(f"subtraction is {sub}")

# MULTIPLICATION
mul = num1 * num2
print(f"multiplication is {mul}")

# DIVISION
div = num1 / num2
print(f"division is {div}")
